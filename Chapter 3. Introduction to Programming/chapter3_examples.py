# If-else statement
home = "America"
if home == "America":
    print("Hello, America!")
else:
    print("Hello world!")
    

# If statement by itself
home = "America"
if home == "America":
    print("Hello, America!")


# Multiple if-statements in a row
x = 2
if x == 2:
    print("The number is 2.")
if x % 2 == 0:
    print("The number is even!")
if x % 2 != 0:
    print("The number is odd.")


# Nesting if-statements
x = 10
y = 11

if x == 10:
    if y == 11:
        print(x + y)


